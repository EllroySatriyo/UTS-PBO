public class Karyawan {
    private int nip;
    private String nama;
    private  String alamat;
    private String tl;
    private String jk;
    private int thn_masuk;
    private int gaji = 2000000;

    public int getNip() {
        return nip;
    }

    public void setNip(int nip) {
        this.nip = nip;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getTl() {
        return tl;
    }

    public void setTl(String tl) {
        this.tl = tl;
    }

    public String getJk() {
        return jk;
    }

    public void setJk(String jk) {
        this.jk = jk;
    }

    public int getThn_masuk() {
        return thn_masuk;
    }

    public void setThn_masuk(int thn_masuk) {
        this.thn_masuk = thn_masuk;
    }

    public int getGaji() {
        return gaji;
    }

    public void setGaji(int gaji) {
        this.gaji = gaji;
    }
}
